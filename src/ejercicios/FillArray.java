package ejercicios;
public class FillArray {
    public static void main(String[] args) {
        int filaNumeros[] = new int[4];
        int columnaNumeros[] = new int[5];
        for (int i = 0; i < filaNumeros.length; i++){
            for (int j = 0; j < columnaNumeros.length; j++){
                System.out.print("5");
            }
            System.out.println();
        }
    }
}
